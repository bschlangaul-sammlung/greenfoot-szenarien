package org.bschlangaul.greenfootscenarios.tetris.firedark60;

import greenfoot.*;

/**
 * A class to hold all the shapes
 *
 * @Brian Huang
 * @June 12th, 2011
 */
public class Shapes extends Actor
{

    /**
     * Method rotate Rotates the shape
     */
    public void rotate()
    {
        this.rotate();

    }

    /**
     * Method checkLeft checks if it is possible to move the shape to the left
     */
    public boolean checkLeft()
    {

        return this.checkLeft();
    }

    /**
     * Method checkRight checks if it is possible to move the shape to the right
     */
    public boolean checkRight()
    {
        return this.checkRight();
    }

    /**
     * Method checkDown checks if it is possible to move the shape to the Down
     */
    public boolean checkDown()
    {
        return this.checkDown();
    }

    /**
     * Method registerShape Registers the Shape into the array
     *
     * @return The coordinates of the shape
     */
    public int[] registerShape()
    {
        int[] x = this.registerShape();

        return x;

    }

}
